		retval = ov5640_af_stop_single(sensor);
		break;
	case V4L2_CID_FOCUS_ABSOLUTE:
		/* Custom implementation that sets the ROI */
		retval = ov5640_af_set_region(sensor, (vc->value >> 16),
					      (vc->value & 0xffff));
		break;
	case V4L2_CID_FOCUS_AUTO:
		retval = ov5640_af_set_auto(sensor, !!vc->value);
		break;
	case V4L2_CID_CONTRAST:
		retval = ov5640_set_contrast(s, vc->value);
		break;
	case V4L2_CID_SATURATION:
		retval = ov5640_set_saturation(s, vc->value);
		break;
	case V4L2_CID_HUE:
		break;
	case V4L2_CID_AUTO_WHITE_BALANCE:
		retval = ov5640_set_autowb(s, vc->value);
		break;
	case V4L2_CID_DO_WHITE_BALANCE:
		retval = ov5640_set_wb(s, vc->value);
		break;
	case V4L2_CID_RED_BALANCE:
		break;
	case V4L2_CID_BLUE_BALANCE:
		break;
	case V4L2_CID_GAMMA:
		break;
	case V4L2_CID_EXPOSURE:
		break;
	case V4L2_CID_AUTOGAIN:
		break;
	case V4L2_CID_GAIN:
		break;
	case V4L2_CID_HFLIP:
