	int ret = 0;

	if (enable)
		ret = ov5640_af_set_mode(sensor, V4L2_CID_FOCUS_AUTO);
	else
		ret = ov5640_af_set_mode(sensor, V4L2_CID_AUTO_FOCUS_STOP);

	return ret;
